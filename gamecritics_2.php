            Földön. Azt már csak később vetette fel az egyik fejlesztő, hogy mi lenne, ha a kiszemelt prédák
            fémdinoszauruszok lennének? Ez az ötlet első hallásra annyira idétlenül hangzott, hogy még a stúdió többi
            munkatársa is őrültnek nézte a kitalálóját. Többek között ezért is volt olyan váratlan, hogy a korábban csak
