            a röhejes alapötlete ellenére egy kifejezetten komoly történetet mesél el, na meg egy vérfagyasztó
            háttérsztorit vázol fel arról, hogy miért fejlődött vissza az emberiség a primitív törzsek szintjére. A
            Horizon Zero Dawn emellett még a szimpatikus főszereplője, a gyönyörű világa és a gépállatdaraboló
            harcrendszere miatt lett népszerű.</p>
        <p class="elsoBetu">Na de ez nem jelenti azt, hogy a Zero Dawn hibátlan játék. A közelharc kifejezetten gyenge
            benne, a karakterek mozgása az átvezetőkben sokszor elég fura, a történet utolsó fejezete kicsit béna, a
            térképikonok utáni rohangálás pedig több tízórányi játék után azért itt is unalmassá tud válni. A rajongók
            viszont annyira kedvelik a hangulatát, hogy simán elnézik ezeket a hiányosságokat (köztük én is), sőt,
            igazából meg is feledkeznek róluk. A Zero Dawnt olyan magas piedesztálra emelte a közönség, hogy szerintem a
            legtöbb embernek eszébe sem jutott, mennyivel szórakoztatóbb játék lehetett volna, ha egy kicsit tovább
            dolgoznak rajta.</p>
        <p class="elsoBetu">A Horizon Forbidden West majdnem (hangsúlyozom: majdnem) miden tekintetben jobb játék, mint
            a Zero Dawn. Ahogy az elődje, ez sem egy forradalmi ötleteket felvonultató alkotás, az egyes elemei pedig
            önmagukban nézve nem fejlődtek olyan sokat. De iszonyatos mennyiségű elemről beszélünk, melyeknek itt közel
            mindegyike magasabb minőséget képvisel, mint egy átlagos open world játékban, és ettől a végeredmény néha
            szabályosan letaglózó, legalábbis az első néhány órában biztosan. Hogy pontosan értsétek, miről is
            zagyválok, alaposan meg kell vizsgálnunk a játékot. Úgyhogy készítsetek be valami üdítőt, mert ez nem lesz
            egy gyors menet!</p>
        <p class="criticsKiemeltSzoveg">A mechanikus vadnyugat romantikája</p>
        <p class="elsoBetu">A nora törzs kitaszítottjaként felnőtt Aloy a Zero Dawn végére hőssé vált, miután elhárított
            egy, a világ pusztulásával fenyegető veszélyt. Ám a harcos amazonnak nincs ideje ünnepelni, mert a krízis
            nem teljesen hárult el, az első játék végén aratott győzelmét követően pedig egyből visszatért az ősök
            romjainak felkutatásához, hogy elejét vehesse egy újabb katasztrófának. A Forbidden West története hat
            hónappal később veszi fel a fonalat, mikor is Aloy rájön, hogy sürgető kérdéseire a Tiltott Nyugaton
            találhat választ. Ez azonban egy kegyetlen vidék, amelyet harcias és a keleti népekkel ellenségesen
            viselkedő törzsek uralnak, és ahol polgárháború robbant ki a fennálló hatalom és a gépek hátán lovagló
            lázadók között. Ráadásul a világ is kezd egyre barátságtalanabbá válni: a termések egyre nagyobb részét
            teszi tönkre egy vörös fertőzés, az időjárás szélsőségesre fordult, a vidéket benépesítő gépek pedig
            megvadulnak. Aloy így kénytelen versenyt futni az idővel, hogy legyőzhessen egy olyan ellenséget, amely
            minden korábbi képzeletét felülmúlja.</p>
        <p class="elsoBetu">Ettől konkrétabban nehéz körbeírni a Forbidden West történetét, mert valami orbitálisan nagy
            fordulatok vannak már az első felében is, szóval nem véletlen, hogy a Guerilla titkolózik a cselekménnyel
            kapcsolatban. A sztori nagyon biztatóan indul, mert rögtön látszik, hogy az írók ezúttal jobban meg akarták
            mutatni, hogy Aloy, a magányosan eltöltött gyerekkorának hála, milyen nehezen alakít ki kapcsolatot
            másokkal: hiába nevezik lépten-nyomon „megmentőnek”, ettől láthatóan kényelmetlenül érzi magát, és hiába
