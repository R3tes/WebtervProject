            is), cserébe viszont egyre jobban elhanyagolja Aloy érzelmi fejlődését. Ráadásul a befejezés felé haladva
            egyre jobban elszállt az írók fantáziája (komoly teljesítmény egy olyan játéktól, melyben íjjal vadászunk
